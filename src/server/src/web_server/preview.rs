/// Legacy route: GET /md-preview/:slug → render markdown directly.
/// New clients use /preview/u/{slug} or /preview/s/{slug}; both dispatch
/// to [`render_md_page`] when the session target is `File`.
pub async fn md_preview_handler(
    Path(slug): Path<String>,
) -> Result<Response, (StatusCode, String)> {
    let entry = common::preview_entries::lookup(&slug)
        .ok_or_else(|| (StatusCode::NOT_FOUND, "Preview not found or expired.".to_string()))?;
    render_md_page(&entry).await
}

/// Render a markdown file preview with toolbar + GitHub CSS + marked.js.
async fn render_md_page(
    entry: &common::preview_entries::PreviewEntry,
) -> Result<Response, (StatusCode, String)> {
    let file_path = match &entry.target {
        PreviewTarget::File => &entry.id,
        PreviewTarget::Server { .. } => {
            return Err((StatusCode::BAD_REQUEST, "This preview serves a server, not a file.".to_string()));
        }
    };

    let content = tokio::fs::read_to_string(file_path).await.map_err(|e| {
        (StatusCode::INTERNAL_SERVER_ERROR, format!("Failed to read file: {e}"))
    })?;

    let remaining_ms = remaining_millis(entry);
    let title = escape_html(&entry.title);

    // Build subtitle: workspace_name / relative_path (or just filename)
    let ws_name = entry.workspace
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or("");
    let subtitle = if let Ok(rel) = file_path.strip_prefix(&entry.workspace) {
        format!("{} / {}", escape_html(ws_name), escape_html(&rel.display().to_string()))
    } else {
        escape_html(ws_name).to_string()
    };

    // Escape the markdown for embedding in a JS template literal.
    let escaped_md = content
        .replace('\\', "\\\\")
        .replace('`', "\\`")
        .replace("${", "\\${")
        .replace("</script>", "<\\/script>");

    let toolbar = toolbar_and_timer(&title, &subtitle, remaining_ms, "");
    let html = format!(
        r#"<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>{title}</title>
<link rel="stylesheet" href="https://cdn.jsdelivr.net/npm/github-markdown-css@5/github-markdown-light.min.css">
<style>
  {toolbar_css}
  body {{
    background: #fff;
    color: #1f2328;
  }}
  .markdown-body {{
    max-width: 880px;
    margin: 0 auto;
    padding: 32px 24px 64px;
  }}
  @media (max-width: 767px) {{
    .markdown-body {{ padding: 16px; }}
  }}
  .markdown-body pre {{
    background: #f6f8fa;
    border-radius: 6px;
    padding: 16px;
    overflow-x: auto;
  }}
  .markdown-body code {{
    font-family: ui-monospace, SFMono-Regular, "SF Mono", Menlo, Consolas, monospace;
    font-size: 85%;
  }}
  .markdown-body pre code {{
    background: transparent;
    padding: 0;
  }}
  .markdown-body table {{
    border-collapse: collapse;
    width: 100%;
  }}
  .markdown-body table th,
  .markdown-body table td {{
    border: 1px solid #d0d7de;
    padding: 6px 13px;
  }}
  .markdown-body table tr:nth-child(2n) {{
    background: #f6f8fa;
  }}
</style>
</head>
<body>
{toolbar}
<article class="markdown-body" id="content"></article>
<script src="https://cdn.jsdelivr.net/npm/marked@15/marked.min.js"></script>
<script>
(function() {{
  var raw = `{raw_md}`;
  document.getElementById('content').innerHTML = marked.parse(raw, {{ gfm: true }});
}})();
</script>
</body>
</html>"#,
        title = title,
        toolbar_css = TOOLBAR_CSS,
        toolbar = toolbar,
        raw_md = escaped_md,
    );
    Ok(html_response(html))
}

// ===========================================================================
// Shared utilities
// ===========================================================================

fn remaining_millis(entry: &common::preview_entries::PreviewEntry) -> u128 {
    entry.expires_at
        .saturating_duration_since(std::time::Instant::now())
        .as_millis()
}

fn escape_html(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
}

fn html_response(html: String) -> Response {
    Response::builder()
        .status(StatusCode::OK)
        .header("Content-Type", "text/html; charset=utf-8")
        .body(Body::from(html))
        .unwrap()
}

/// Toolbar HTML + countdown timer script.
fn toolbar_and_timer(title: &str, subtitle: &str, remaining_ms: u128, extra_buttons: &str) -> String {
    let subtitle_html = if subtitle.is_empty() {
        String::new()
    } else {
        format!(r#"<span class="subtitle">{}</span>"#, subtitle)
    };
    format!(
        r#"<div class="toolbar">
  <span class="title">{title}</span>
  {subtitle_html}
  <span class="badge" id="timer">5:00</span>
  <span class="spacer"></span>
  {extra_buttons}
</div>
<script>
(function() {{
  var expiry = Date.now() + {remaining_ms};
  var el = document.getElementById('timer');
  setInterval(function() {{
    var left = Math.max(0, expiry - Date.now());
    var m = Math.floor(left / 60000);
    var s = Math.floor((left % 60000) / 1000);
    el.textContent = m + ':' + (s < 10 ? '0' : '') + s;
    if (left <= 0) {{
      el.textContent = 'Expired';
      el.style.background = '#6b2020';
      el.style.color = '#e5a5a5';
    }}
  }}, 1000);
}})();
</script>"#,
        title = title,
        subtitle_html = subtitle_html,
        remaining_ms = remaining_ms,
        extra_buttons = extra_buttons,
    )
}


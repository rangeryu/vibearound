//! Live preview routes.
//!
//! **Preview iframe** (cookie-based, transparent proxy inside iframe):
//! - GET /preview/u/:slug            — owner preview (iframe + set cookie)
//! - GET /preview/s/:slug            — share preview (iframe + set cookie)
//!   Both set a `va_preview` cookie and render an iframe with `src="/"`.
//!   The iframe content is served by the cookie proxy fallback at root.
//!
//! **Markdown preview**:
//! - GET /md-preview/:slug           — rendered markdown page
//!
//! **Cookie proxy fallback** (root `/` handler):
//!   - Has cookie + from iframe → proxy to dev server
//!   - Has cookie + direct navigation (`Sec-Fetch-Dest: document`) → error page
//!   - No cookie → redirect to `/va/`
//!
//! Slugs are short-lived (5–10 min) and generated by the `preview` /
//! `md_preview` MCP tools. The slug itself acts as authentication.

use axum::{
    extract::{Path, Request, State},
    http::StatusCode,
    response::{IntoResponse, Redirect, Response},
};
use axum::body::Body;

use common::preview_entries::PreviewTarget;

use super::AppState;

// ===========================================================================
// Preview iframe — sets cookie, renders iframe with src="/"
// ===========================================================================

/// Cookie name for authenticated owner sessions (set by /pair flow).
const OWNER_COOKIE: &str = "va_owner";

/// GET /preview/u/{slug} — owner preview. Requires `va_owner` cookie.
///
/// Dispatches by session target: Server → iframe + cookie proxy,
/// File → rendered markdown. If the cookie is missing or invalid,
/// redirects to `/va/?next=<original-url>` so the pairing gate can
/// send the user back to their intended destination.
pub async fn owner_preview_handler(
    Path(slug): Path<String>,
    req: Request,
) -> Result<Response, (StatusCode, String)> {
    if !owner_cookie_valid(&req) {
        // `req.uri().path_and_query()` sees the path AFTER the `/va` nest
        // prefix has been stripped by axum, so prepend `/va` to rebuild the
        // absolute URL the browser should land on after pairing.
        let inner = req
            .uri()
            .path_and_query()
            .map(|pq| pq.as_str().to_string())
            .unwrap_or_else(|| format!("/preview/u/{}", slug));
        let next = format!("/va{}", inner);
        let location = format!("/va/?next={}", url_encode_query(&next));
        return Ok(Response::builder()
            .status(StatusCode::FOUND)
            .header("Location", location)
            .body(Body::empty())
            .unwrap());
    }
    render_preview(&slug).await
}

/// Minimal percent-encoder for query-string values: encodes anything
/// outside the unreserved set so the URL stays well-formed.
fn url_encode_query(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for b in s.as_bytes() {
        match b {
            b'A'..=b'Z' | b'a'..=b'z' | b'0'..=b'9' | b'-' | b'_' | b'.' | b'~' => {
                out.push(*b as char);
            }
            _ => out.push_str(&format!("%{:02X}", b)),
        }
    }
    out
}

/// GET /preview/s/{slug} — share preview. Slug itself is the auth.
///
/// Dispatches by session target: Server → iframe + cookie proxy,
/// File → rendered markdown. No pairing required — the random share
/// key (10-min TTL) gates access.
pub async fn share_preview_handler(
    Path(slug): Path<String>,
) -> Result<Response, (StatusCode, String)> {
    render_preview(&slug).await
}

/// Check whether the request carries a valid `va_owner` cookie.
fn owner_cookie_valid(req: &Request) -> bool {
    let token = match extract_cookie(req, OWNER_COOKIE) {
        Some(t) => t,
        None => return false,
    };
    common::auth::read_token_file()
        .map(|f| f.token == token)
        .unwrap_or(false)
}

/// Look up a preview by slug and dispatch to the right renderer based on target.
async fn render_preview(slug: &str) -> Result<Response, (StatusCode, String)> {
    let entry = common::preview_entries::lookup(slug)
        .ok_or_else(|| (StatusCode::NOT_FOUND, "Preview not found or expired.".to_string()))?;

    match entry.target {
        PreviewTarget::Server { port } => render_server_iframe(slug, &entry, port).await,
        PreviewTarget::File => render_md_page(&entry).await,
    }
}

/// Render the iframe wrapper for a Server preview; sets `va_preview` cookie
/// so the root-level cookie proxy can route requests to `localhost:{port}`.
async fn render_server_iframe(
    slug: &str,
    entry: &common::preview_entries::PreviewEntry,
    port: u16,
) -> Result<Response, (StatusCode, String)> {
    let remaining_ms = remaining_millis(entry);
    let remaining_secs = (remaining_ms / 1000) as u64;
    let title = escape_html(&entry.title);
    let subtitle = format!(":{}", port);

    let cookie_value = format!(
        "{}={}; Path=/; Max-Age={}; SameSite=Lax",
        PREVIEW_COOKIE, slug, remaining_secs
    );

    let toolbar = toolbar_and_timer(
        &title,
        &subtitle,
        remaining_ms,
        r#"<button onclick="document.querySelector('iframe').src=document.querySelector('iframe').src">Refresh</button>"#,
    );
    let html = format!(
        r#"<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>Preview — {title}</title>
<style>
  {toolbar_css}
  body {{ background: #111; }}
  iframe {{
    width: 100%;
    height: calc(100vh - 40px);
    border: 0;
    background: #fff;
  }}
</style>
</head>
<body>
{toolbar}
<iframe src="/"></iframe>
</body>
</html>"#,
        title = title,
        toolbar_css = TOOLBAR_CSS,
        toolbar = toolbar,
    );

    Ok(Response::builder()
        .status(StatusCode::OK)
        .header("Content-Type", "text/html; charset=utf-8")
        .header("Set-Cookie", cookie_value)
        .body(Body::from(html))
        .unwrap())
}

